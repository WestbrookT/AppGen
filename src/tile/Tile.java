package tile;

public class Tile {
	
	private int r;
	private int g;
	private int b;
	private int maxR;
	private int maxG;
	private int maxB;
	
	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public int getG() {
		return g;
	}

	public void setG(int g) {
		this.g = g;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getMaxR() {
		return maxR;
	}

	public void setMaxR(int maxR) {
		this.maxR = maxR;
	}

	public int getMaxG() {
		return maxG;
	}

	public void setMaxG(int maxG) {
		this.maxG = maxG;
	}

	public int getMaxB() {
		return maxB;
	}

	public void setMaxB(int maxB) {
		this.maxB = maxB;
	}
}
